package com.hmp.localvariables;

public class LocalVariables {
    public static void main(String[] args) {
        int age=20;
        float loanAmount = 200.78f;
        double loanTaken = 18977.5d;
        boolean isLoanPaid = true;
        System.out.println("Integer variable: "+age);
        System.out.println("Floating point variable: "+loanAmount);
        System.out.println("Double type variable value: "+loanTaken);
        System.out.println("Boolean type variable value: "+isLoanPaid);

    }
}
